    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        TelaLogin telaLogin = new TelaLogin();
        telaLogin.setVisible(true);
        
        String nomeUsuario = txtNome.getText();
        String loginUsuario = txtLogin.getText();
        String senhaUsuario = txtSenha.getText();
        
        Login login = new Login();
        
        login.setNome(nomeUsuario);
        login.setLogin(loginUsuario);
        login.setSenha(senhaUsuario);
        
        LoginDao loginDao = new LoginDao();
        loginDao.inserir(login);
      
        JOptionPane.showMessageDialog(null, "Conta criada com sucesso, por favor, acesse a conta usando as credencias fornecidas");
        dispose();
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

